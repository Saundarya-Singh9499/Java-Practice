package conditionalandloops;

import java.util.Scanner;

public class WhileLoop {

public static void main(String[] args) {
	 
	Scanner s= new Scanner(System.in);
    int a = s.nextInt();
    
    int i = 1;
    while( i <= a) {
    	System.out.println("Hello World");
        
    	i = i + 1;
    }
     
}
}
